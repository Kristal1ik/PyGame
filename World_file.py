import pygame

bg = pygame.image.load('game_imgs\Bg.png')


class World:
    def __init__(self, data, tile, screen):
        # ОБНОВЛЕН ПУТЬ
        # изменено название изображения, тк при написании \b может быть ошибка

        self.screen = screen
        self.tilelst = []
        # ОБНОВЛЕН ПУТЬ
        # изменено название изображения, тк при написании \d может быть ошибка
        dirt = pygame.image.load('game_imgs\Dirt.jpg')
        n_row = 0
        for i in data:
            n = 0
            for j in i:
                if j == '.' or j == 2:
                    img = pygame.transform.scale(dirt, (tile, tile))
                    rect = img.get_rect()
                    rect.x = n * tile
                    rect.y = n_row * tile
                    j = (img, rect)
                    self.tilelst.append(j)
                n += 1
            n_row += 1

    def draw(self):
        for i in self.tilelst:
            self.screen.blit(i[0], i[1])


